# simple math in python

Integer = 1

print(Integer + 2)
print(Integer - 2)
print(Integer * 2)
print(Integer / 2)
# common math functions can be performed
# all math functions return an integer other than division
# division always returns a float

print(Integer ** 2)
# ** works as an exponent

print(Integer % 2)
# % is modulus
# modulus returns the remainder when numbers are divided

Integer += 1
# += will increase a given variable using augmented assignment

Integer -= 1
Integer *= 1
Integer /= 1
Integer **= 1
Integer %= 1
# augmented assignment can be applied to the other math functions

print(10 + 10 * 2)
# order of operations is automatic in python
