# math in python

import math
# math module is built into python

print(math.ceil(1.5))
# ceil() will return a float rounded up as an integer

print(math.floor(1.5))
# floor() will return a float rounded down as an integer

print(math.fabs(-1))
# fabs() will return the absolute value as a float

print(math.gcd(12, 8))
# gcd() will return the greatest common divisor of two numbers as an integer

print(math.sqrt(16))
# sqrt() will return the square root of a given number as a float

print(math.sin(1))
# sin() will return the sin of a given number in radians as a float

print(math.pi)
# pi will return the value of pi to a certain precision

print(math.e)
# e will return the value of e to a certain precision
